package model;

public class OperationInfo {

	private int waitOther=1;

	private int start=0;
	
	
	public int getWaitOther() {
		return waitOther;
	}

	public void setWaitOther(int waitOther) {
		this.waitOther = waitOther;
	}

	public int getStart() {
		return start;
	}

	public void setStart(int start) {
		this.start = start;
	}
	
	
	
}
